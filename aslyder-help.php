	<h2>Options</h2>
	<p>
	<b>Number of posts</b><br />This is the number of posts to pull from category <i>aslyder</i> to be included in the slideshow. Slides are pulled from the category by date, in the order of newest to oldest.
	</p>
	
	<p>
		<b>Slider transition type</b><br />
		Sets the animation type to one of three.
		<ul>
			<li>&bull; Slide (default) - slides move as a group</li>
			<li>&bull; Fade: slides fade into one another</li>
			<li>&bull; Peel: Slides are pulled off the stack to reveal the next slide in the stack below</li>
		</ul>
	</p>
	
	<p>
		<b>Slide reset type</b><br />
		This controls the slider reaction to reaching the last slide. Snapback animates the entire slider to quickly reverse motion to return to the first slide.  Continuous flow gives the illusion of the slideshow continuing infinitely in one direction.
	</p>
	
	<p>
		<b>Peel direction</b><br />
		The direction the slides are peeled off the stack. Only applies to peel-type transition. If set to fade or slide, this setting has no effect.
	</p>
	
	<p>
		<b>Transition speed</b><br />
		The amount of time for the animation effect between slides.  Setting is in seconds. Fractional (decimal) seconds are allowed.
	</p>
	
	<p>
		<b>Slide pause</b><br />
		The amount of time a slide stays on display before triggering the transition to the next slide. Amount is in seconds. Fractional seconds are allowed.
	</p>
	
	<p>
		<b>Slide source</b><br />
		Where the image that will be used as the slide is pulled from. Either the first image in the post content or the featured image for the post.
	</p>
	
	<p>
		<b>Display navigation buttons</b><br />
		If set a LI of numbered links for each slide will be displayed.  This list is loaded directly following the slideshow container.
	</p>
	
	<p>
		<b>Display advancing buttons</b><br />
		Shows buttons (links) for Previous and Next slides.
	</p>
	
	<p>
		<b>Disable linking to post</b><br />
		By default, all slides are linked to the post they are drawn from.  If the slideshow is for display only, and the articles are not to be part of the site content, disabling this will remove the link from the slide to the post.
	</p>
	
	<p>
		<b>Disable autostart</b><br />
		The slideshow will normally begin automatically when the page finishes loading. Checking to disable will leave the slides to move only on user input. This requires one or both of the buttons sets to be enabled.
	</p>
	